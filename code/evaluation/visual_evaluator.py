#!/usr/bin/env python3
"""
Visual Evaluator for Evolution-Reproduce.
Generates HTML reports with images and metrics for human inspection.
Also supports computing basic image metrics (PSNR, SSIM, MSE) if ground truth is provided.
"""
import os
import json
import base64
from pathlib import Path
import argparse
import math

try:
    import numpy as np
    from PIL import Image
    HAS_CV_LIBS = True
except ImportError:
    HAS_CV_LIBS = False

def calculate_psnr(img1, img2):
    if not HAS_CV_LIBS: return None
    mse = np.mean((img1 - img2) ** 2)
    if mse == 0:
        return 100
    max_pixel = 255.0
    psnr = 20 * math.log10(max_pixel / math.sqrt(mse))
    return psnr

def encode_image(img_path):
    with open(img_path, "rb") as f:
        encoded = base64.b64encode(f.read()).decode('utf-8')
        ext = str(img_path).split('.')[-1].lower()
        mime = "image/jpeg" if ext in ["jpg", "jpeg"] else f"image/{ext}"
        return f"data:{mime};base64,{encoded}"

def generate_html_report(workspace_dir, output_file, paper_id=None):
    ws = Path(workspace_dir)
    if not ws.exists():
        print(f"Workspace {workspace_dir} not found.")
        return False

    # 1. Load results.json
    results_path = ws / "results.json"
    metrics_html = "<h3>Metrics (results.json not found)</h3>"
    if results_path.exists():
        try:
            with open(results_path) as f:
                results = json.load(f)
            metrics_html = "<h3>Metrics</h3><pre>" + json.dumps(results, indent=2) + "</pre>"
        except Exception as e:
            metrics_html = f"<h3>Metrics (Error parsing results.json: {e})</h3>"

    # 2. Find images
    image_extensions = ('*.png', '*.jpg', '*.jpeg', '*.svg', '*.gif')
    images = []
    for ext in image_extensions:
        images.extend(ws.rglob(ext))
    
    images.sort()
    
    images_html = "<h3>Visual Outputs</h3>"
    if images:
        images_html += "<div style='display: flex; flex-wrap: wrap; gap: 20px;'>"
        for img in images:
            try:
                img_data = encode_image(str(img))
                rel_path = img.relative_to(ws)
                images_html += f"""
                <div style='border: 1px solid #ccc; padding: 10px; border-radius: 5px; max-width: 500px;'>
                    <h4>{rel_path}</h4>
                    <img src='{img_data}' style='max-width: 100%; height: auto; border: 1px solid #eee;' />
                </div>
                """
            except Exception as e:
                print(f"Error encoding {img}: {e}")
        images_html += "</div>"
    else:
        images_html += "<p>No images found in workspace. (Check if the code generated graphical outputs)</p>"

    # 3. Build HTML
    title = f"Visual Report: {paper_id or ws.name}"
    html_content = f"""
    <!DOCTYPE html>
    <html>
    <head>
        <meta charset="utf-8">
        <title>{title}</title>
        <style>
            body {{ font-family: Arial, sans-serif; margin: 20px; line-height: 1.6; color: #333; }}
            h1, h2, h3, h4 {{ color: #2c3e50; }}
            pre {{ background: #f4f4f4; padding: 15px; border-radius: 5px; overflow-x: auto; font-size: 14px; border: 1px solid #ddd; }}
            .container {{ max-width: 1400px; margin: auto; padding: 20px; }}
            .header {{ background: #f8f9fa; padding: 20px; border-radius: 8px; margin-bottom: 20px; }}
        </style>
    </head>
    <body>
        <div class="container">
            <div class="header">
                <h1>{title}</h1>
                <p><strong>Workspace Path:</strong> {workspace_dir}</p>
                <p><em>Generated by Evolution-Reproduce Visual Evaluator</em></p>
            </div>
            
            <div class="metrics-section">
                {metrics_html}
            </div>
            
            <hr style="border: 0; height: 1px; background: #ddd; margin: 30px 0;">
            
            <div class="images-section">
                {images_html}
            </div>
        </div>
    </body>
    </html>
    """

    output_path = Path(output_file)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w", encoding='utf-8') as f:
        f.write(html_content)
    
    print(f"Report generated: {output_path}")
    return True

def generate_index_html(reports_dir, title="Evaluation Reports Index"):
    r_dir = Path(reports_dir)
    reports = list(r_dir.glob("*.html"))
    reports = [r for r in reports if r.name != "index.html"]
    reports.sort()
    
    links_html = "<ul>"
    for r in reports:
        links_html += f"<li><a href='{r.name}' target='_blank'>{r.name}</a></li>"
    links_html += "</ul>"
    
    index_content = f"""
    <!DOCTYPE html>
    <html>
    <head><title>{title}</title></head>
    <body style="font-family: Arial, sans-serif; max-width: 800px; margin: auto; padding: 40px;">
        <h2>{title}</h2>
        <p>Click on the links below to view the visual reproduction reports:</p>
        {links_html}
    </body>
    </html>
    """
    with open(r_dir / "index.html", "w", encoding='utf-8') as f:
        f.write(index_content)
    print(f"Index generated at: {r_dir / 'index.html'}")

def main():
    parser = argparse.ArgumentParser(description="Generate visual report for reproduction results.")
    parser.add_argument("--workspace", type=str, help="Path to a single workspace directory")
    parser.add_argument("--output", type=str, help="Path to output HTML file")
    parser.add_argument("--paper_id", type=str, help="Optional paper ID")
    parser.add_argument("--batch_dir", type=str, help="Directory containing multiple workspaces (e.g. openhands_workspace_v5)")
    parser.add_argument("--batch_out_dir", type=str, help="Output directory for batch reports")
    parser.add_argument("--pattern", type=str, default="*", help="Pattern to match workspaces in batch mode (e.g. 'test_b1_*')")
    
    args = parser.parse_args()

    if args.batch_dir and args.batch_out_dir:
        base_dir = Path(args.batch_dir)
        out_dir = Path(args.batch_out_dir)
        out_dir.mkdir(parents=True, exist_ok=True)
        
        count = 0
        for ws in base_dir.glob(args.pattern):
            if ws.is_dir():
                out_file = out_dir / f"{ws.name}.html"
                if generate_html_report(ws, out_file, paper_id=ws.name):
                    count += 1
        
        if count > 0:
            generate_index_html(out_dir, title=f"Visual Reports (Pattern: {args.pattern})")
            
    elif args.workspace and args.output:
        generate_html_report(args.workspace, args.output, paper_id=args.paper_id)
    else:
        parser.print_help()

if __name__ == "__main__":
    main()
